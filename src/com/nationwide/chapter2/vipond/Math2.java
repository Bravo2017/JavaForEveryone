package com.nationwide.chapter2.vipond;

public class Math2 {
	// Create a class and use four different Math static operations

	public static void main(String[] args) {
		double sqrt = Math.sqrt(49);
		System.out.println(sqrt);

		int pow = (int) Math.pow(8, 2);
		System.out.println(pow);

		int abs = Math.abs(-323);
		System.out.println(abs);

		int max = Math.max(999, 851);
		System.out.println(max);

	}
}
