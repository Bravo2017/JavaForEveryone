package com.nationwide.chapter2.vanwyk;

public class MathStatic {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// to compute the square root
		double a = (Math.sqrt(100));

		System.out.println(a);

		// to compute the power of
		double b = (Math.pow(10, 4));

		System.out.println(b);

		// to compute the sine
		double c = (Math.sin(5));

		System.out.println(c);

		// to compute the tangent
		double d = (Math.tan(40));

		System.out.println(d);

	}
}