public class Suit {
	final String club = "CLUB";
	final String spade = "SPADE";
	final String diamond = "DIAMOND";
	final String heart = "HEART";

	public String getClub() {
		return club;
	}

	public String getDiamond() {
		return diamond;
	}

	public String getHeart() {
		return heart;
	}

	public String getSpade() {
		return spade;
	}
}
