package com.nationwide.chapter3.ron;

public class JFEChapter3If {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int x = 10;
		int y = 5;
		int z = 20;

		if (x > 5) {
			System.out.println("X > 5");
		} else {
			System.out.println("X not > 5");
		}
		if (y < 10) {
			System.out.println("y < 10");
		} else {
			System.out.println("y not < 10");
		}
		if (z == 15) {
			System.out.println("z = 15");
		} else {
			System.out.println("z not = 15");
		}
		if (z != 20) {
			System.out.println("z not = 20");
		} else {
			System.out.println("z = 20");
		}

	}

}
